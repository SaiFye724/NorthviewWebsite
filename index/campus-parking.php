                    <h3>CAMPUS PARKING</h3>
                    <hr>
                    <h4><strong>GENERAL INFORMATION AND FREQUENTLY ASKED QUESTIONS</strong></h4>

                    <p>Students who have had ISS or OSS disciplinary actions during the previous semester will automatically have their name removed from the list of eligible parking candidates. They will not qualify for parking during the semester following ISS/OSS discipline.</p>

                    <ul>
                        <li><b>When can I apply for parking?</b><br />
<i>You can apply for parking during the summer and anytime during the school year. <b>Parking applications are available under the "EXTERNAL LINKS" section on the top left of this page.</b></i></li>
                        <p>
                        <li><b>How are the parking spaces assigned?</b><br />
<i>Parking spaces are assigned on a first come first served basis and availability.</i></li>
                        <p>
                        <li><b>When do I pick up my parking decal?</b><br />
<i>If you applied during the summer, you will pick up your decal and parking space on Information Day, Thursday August 7th during your scheduled grade level time.</i></li>
                        <p>
                        <li><b>I do not have my license yet. Should I apply for a parking permit?</b><br />
<i>No</i></li>
                        <p>
                        <li><b>If I do not pick up my decal before the 1st day of the semester, where do I park the first day of school?</b><br />
<i>You are not allowed to drive and park the first day of school if you have not picked up your parking decal. Pick up your decal at lunch on the first day of school and then start driving to school on the second day.</i></li>
                        <p>
                        <li><b>How do students lose their parking permit?</b><br />
<i>Students lose their permit by not following the rules they are given when they pick up their permit. Be familiar with these rules. The biggest infraction is parking in a different space than the one assigned to them or in the visitor’s parking lot.</i></li>
                        <p>
                        <li><b>Can I park in visitors to go to my College English/Math class or to attend a meeting?</b><br />
<i>No. Students are not allowed to park in the visitor’s lot at any time for any reason before or during school. These few slots are reserved for parents and other visitors.</i></li>
</ul>

                    <h4><strong>NORTHVIEW HIGH SCHOOL PARKING  RULES AND REGULATIONS</strong></h4>
                    <ol>
                        <li>Students must have a valid parking sticker to park on campus during the school day. This sticker must be permanently placed in the lower right back windshield (passenger  side) of the car for which it has been issued. Backing into parking spaces is NOT allowed. The parking sticker must be visible from the back of vehicle when the vehicle is parked.</li></p>
                        <p>
                        <li>Students receiving parking permits accept responsibility to  following all school rules and regulations. Suspension of driving privileges, booting of vehicles and/or suspension from school may occur when violations of  these rules occur. The parking lot will be checked periodically. (<u>1st  offense</u>-loss of parking for one week; <u>2nd offense</u>-loss of privilege for two weeks; <u>3rd offense</u>-loss  of privilege for the remainder of the school year).</li></p>
                        <p>
                        <li>Parking permits are non-transferable. Students found purchasing, selling, exchanging, altering or counterfeiting permits will have their privileges revoked and subject to suspension.</li></p>
                        <p>
                        <li>Cars will be booted that are illegally parked when the owner cannot be identified. Repeat offenders will have their parking permits revoked and/or other disciplinary consequences.<br />
                        <br />
                        The booting device would be placed on the car's tire and a notice placed on the windshield explaining that the car has been booted and should not be moved until the boot is removed. Damage will occur if the car is driven while the boot is in place. Removal would require the owner to contact the campus police and pay a $50 cash fine. This is in lieu of the towing charges which can be in excess of $100.</li></p>
                        <p>
                        <li>TEMPORARY PARKING PERMITS are issued when a student must drive a car to school other than his/her car that is registered with the school. In this case, the student must obtain a temporary permit from the front office between 7:50 and 8:15 and display this permit on the right side of the dash. Students must allow ample time to obtain a temporary permit and return to their car to place it on the dash before their first class or student will be marked tardy.</li></p>
                        <p>
                        <li>There is no loitering in the parking lot. Students are not permitted to return to their cars prior to school dismissal without permission from an administrator. </li></p>
                        <p><li>All passengers, including the driver MUST have a seat belt on before leaving the student parking lot. Failure to adhere to this law will result in disciplinary action, which may include loss of parking privileges. </li></p>
                        <p><li>Students will observe the 15 MPH speed limit while on school grounds. Students who drive recklessly on school grounds or within 1,000 feet of school grounds are subject to disciplinary action and/or loss of parking permit.</li></p>
                        <p><li>Student vehicles are subject to search if there are reasonable grounds to believe that drugs, alcohol, stolen property or contraband might be present in the vehicle. Failure to comply with a vehicle search will result in permanent forfeiture of parking privileges and suspension. </li></p>
                        <p><li>Students who skip school/class, violate parking regulations or drive in a reckless manner will have their permit revoked for a period of time to be determined by the school administration. Students who have their permit revoked are not eligible to receive a refund. </li></p>
                        <p><strong><em>Parking Decals may be revoked for the  following reasons:</em></strong></p>
                        <ul type="disc">
                          <li>10 or more tardies in a semester</li>
                          <li>10 or more absences in a semester</li>
                          <li>ISS or OSS assigned in the current semester</li>
                        </ul>
                        <p><li>Reinstating Parking Privileges: Students will not be guaranteed the same parking space when the decal is re-issued. The parking decal may be re-issued at the end of the revoked time period if space is available. If a parking space is not available at the end of the parking suspension the student will be put on the waiting list for an available parking space.</li></p>
                        <p><li>The school is not responsible for the automobile or its contents. </li></p>
                        <p><strong><em>**If your assigned space is occupied, do NOT park in a space not assigned to you or in a space designated as “STAFF”. Write down the license plate number and decal number of the car in your assigned space and report the problem immediately to the front office.</em></strong></p>
                        <p>Revised 5/8/2015</p><p>&nbsp;</p>
